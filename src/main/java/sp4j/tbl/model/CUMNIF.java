package sp4j.tbl.model;

public class CUMNIF {
	/** 메뉴_아이디 */
	private String menuId;
	
	/** 메뉴_페이지_아이디 */
	private String menuPgId;
	
	/** 메뉴_페이지_이름*/
	private String menuPgNm;
	
	/** 메뉴_페이지_경로*/
	private String menuPgUrl;

	public String getMenuId() {
		return menuId;
	}

	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}

	public String getMenuPgId() {
		return menuPgId;
	}

	public void setMenuPgId(String menuPgId) {
		this.menuPgId = menuPgId;
	}

	public String getMenuPgNm() {
		return menuPgNm;
	}

	public void setMenuPgNm(String menuPgNm) {
		this.menuPgNm = menuPgNm;
	}

	public String getMenuPgUrl() {
		return menuPgUrl;
	}

	public void setMenuPgUrl(String menuPgUrl) {
		this.menuPgUrl = menuPgUrl;
	}
	
	
}
